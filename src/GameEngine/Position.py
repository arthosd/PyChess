"""
Script qu traite la position
"""

pattern_letter = {
    "a": 0,
    "b": 1,
    "c": 2,
    "d": 3,
    "e": 4,
    "f": 5,
    "g": 6,
    "h": 7,
}

pattern_number = {
    "8": 0,
    "7": 1,
    "6": 2,
    "5": 3,
    "4": 4,
    "3": 5,
    "2": 6,
    "1": 7,
}


def tulpe_position(letter, number):
    return (pattern_letter[letter], pattern_number[number])
